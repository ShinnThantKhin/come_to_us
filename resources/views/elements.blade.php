                                <h2>Elements of ComeToUs</h2>
                              <p>There are many models and types of therapy to choose from. We believe there are a handful of common denominators present in all forms of healthy, ethical therapy. These elements are described here.</p>

                              <h3>NONPATHOLOGIZING</h3>
                              <p>Viewing a person as greater than his or her problems is the hallmark of nonpathologizing therapy.
                                It does not mean problems do not exist. Rather, it means one does not view the problems as the whole person.
                                Working nonpathologically requires a shift in both the understanding and the approach to pathology.</p>

                              <h3>RELATIONSHIP</h3>
                              <p>Beyond technique and theory is the realm of the relationship: the ongoing human-to-human connection that provides the foundation for change.
                                The therapeutic relationship is the safe container that allows one to more fully and completely feel the presence of Self while in the presence of another.
                                Our therapists embodied and feels unconditional positive regard in the face of whatever the person in treatment may be experiencing nurtures the therapeutic relationship.</p>

                              <h3>WE ARE NOT PERFECT<h3>
                              <p>No therapist is perfect, and no therapy can be provided perfectly, no matter how ideal a therapy may be in theory.
                                Like any profession, therapists are human beings and therefore subject to limitations
                                However, our therapists are trained professionals who have undergone extensive education and supervised practice to provide effective and ethical counseling services.
                                We strive to create a safe and supportive environment for our clients to explore their thoughts and feelings, and we work to help our clients develop the skills and insight needed to address their challenges.
                                It's also important to note that counseling and therapy are not one-size-fits-all solutions, and what works for one person may not work for another.
                                This means that sometimes, even with the best therapist and intentions, counseling and therapy may not produce the desired results.
                                In these cases, it may be helpful to try a different approach or seek out a different therapist.<a href="#"> Click here to meet our therapists</a></p>

